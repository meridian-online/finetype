-- GitTables 1M Evaluation: FineType CLI pipeline (NNFT-130)
-- =============================================================================
-- Scores CLI batch predictions against ground truth using the same schema
-- mapping and accuracy metrics as eval_1m.sql, but reads pre-computed
-- predictions from cli_predictions.csv instead of running the DuckDB extension.
--
-- Pipeline:
--   1. extract_metadata_1m.py  — Extract annotations from parquet KV metadata
--   2. prepare_1m_values.py    — Sample column values from parquet files
--   3. eval_cli.py             — Classify via CLI batch mode (tiered + Model2Vec)
--   4. This script             — Score predictions against ground truth
--
-- Usage:
--   make eval-1m-cli                                    # Via Makefile (recommended)
--   EVAL_OUTPUT=~/datasets/gittables/eval_output envsubst '$EVAL_OUTPUT' < eval/gittables/eval_cli.sql | duckdb
--
-- Path variables (substituted by envsubst via Makefile):
--   ${EVAL_OUTPUT}     — Directory with catalog.csv, metadata.csv, cli_predictions.csv
--
-- Prerequisites:
--   1. Pre-extracted metadata: make eval-extract eval-values
--   2. CLI predictions: eval/gittables/eval_cli.py
--   3. Schema mapping generated: make eval-mapping

SET threads = 8;
SET memory_limit = '4GB';

.mode box
.timer on

-- ═══════════════════════════════════════════════════════════════════════════════
-- 1. LOAD PRE-EXTRACTED METADATA
-- ═══════════════════════════════════════════════════════════════════════════════

.print ''
.print '═══════════════════════════════════════════════════════════════════'
.print '          GITTABLES 1M CLI — LOADING METADATA                    '
.print '═══════════════════════════════════════════════════════════════════'

CREATE OR REPLACE TABLE catalog AS
SELECT * FROM read_csv('${EVAL_OUTPUT}/catalog.csv', auto_detect=true);

CREATE OR REPLACE TABLE metadata AS
SELECT * FROM read_csv('${EVAL_OUTPUT}/metadata.csv', auto_detect=true);

SELECT
    (SELECT sum(total_tables) FROM catalog) AS total_corpus_tables,
    (SELECT count(DISTINCT topic) FROM catalog) AS total_topics,
    count(*) AS sampled_tables,
    sum(CASE WHEN n_annotated_cols > 0 THEN 1 ELSE 0 END) AS annotated_tables,
    ROUND(sum(CASE WHEN n_annotated_cols > 0 THEN 1 ELSE 0 END) * 100.0 / count(*), 1) AS annotation_pct,
    sum(n_annotated_cols) AS total_annotated_cols
FROM metadata;

-- Flatten annotations: one row per (table, column, gt_label)
CREATE OR REPLACE TABLE ground_truth AS
SELECT
    m.topic,
    m.table_name,
    m.file_path,
    j.key AS col_name,
    j.value AS gt_label
FROM metadata m,
LATERAL (
    SELECT
        unnest(json_keys(m.annotations_json::JSON)) AS key,
        unnest(json_extract_string(m.annotations_json::JSON, '$.*'))
) j(key, value)
WHERE m.annotations_json IS NOT NULL AND m.annotations_json != '';

SELECT
    count(*) AS total_gt_annotations,
    count(DISTINCT gt_label) AS unique_gt_labels,
    count(DISTINCT topic) AS topics_with_gt
FROM ground_truth;

.print ''
.print '--- Top 20 ground truth labels ---'
SELECT gt_label, count(*) AS columns
FROM ground_truth
GROUP BY gt_label
ORDER BY columns DESC
LIMIT 20;

