#!/usr/bin/env python3
"""FineType Evaluation Report Generator (NNFT-147)

Generates eval/eval_output/report.md — a unified markdown dashboard combining
profile eval accuracy, actionability results, and taxonomy coverage stats.

Usage:
    python3 eval/eval_report.py                           # defaults
    python3 eval/eval_report.py --output path/report.md   # custom output

Prerequisites:
    1. Profile results: eval/eval_output/profile_results.csv
    2. Ground truth: eval/eval_output/ground_truth.csv
    3. Schema mapping: eval/schema_mapping.csv
    4. Actionability results: eval/eval_output/actionability_results.csv
       (generated by eval/eval_actionability.py)
    5. Taxonomy YAML files in labels/
"""

import argparse
import csv
import sys
from collections import defaultdict
from datetime import datetime
from pathlib import Path

import yaml


def load_csv(path: Path) -> list[dict]:
    """Load a CSV file as list of dicts."""
    if not path.exists():
        return []
    with open(path) as f:
        return list(csv.DictReader(f))


def load_schema_mapping(path: Path) -> dict[str, dict]:
    """Load schema mapping keyed by gt_label."""
    rows = load_csv(path)
    mapping = {}
    for row in rows:
        mapping[row["gt_label"]] = row
    return mapping


def load_taxonomy_stats(labels_dir: Path) -> dict:
    """Compute taxonomy coverage stats from YAML definitions."""
    stats = {
        "total_types": 0,
        "with_format_string": 0,
        "with_validation": 0,
        "with_locale_validation": 0,
        "with_transform": 0,
        "domains": defaultdict(int),
    }
    for yaml_file in sorted(labels_dir.glob("definitions_*.yaml")):
        data = yaml.safe_load(yaml_file.read_text())
        for key, val in data.items():
            if not isinstance(val, dict):
                continue
            stats["total_types"] += 1
            domain = key.split(".")[0] if "." in key else "unknown"
            stats["domains"][domain] += 1
            if val.get("format_string") and val["format_string"] != "null":
                stats["with_format_string"] += 1
            if val.get("validation"):
                stats["with_validation"] += 1
            if val.get("validation_by_locale"):
                stats["with_locale_validation"] += 1
            if val.get("transform") and val["transform"] != "null":
                stats["with_transform"] += 1
    return stats


def is_label_match(predicted: str, expected: str) -> bool:
    """Check label match with interchangeability rules (matches eval_profile.sql)."""
    if predicted == expected:
        return True
    # Boolean sub-types are interchangeable
    if expected.startswith("representation.boolean.") and predicted.startswith("representation.boolean."):
        return True
    # Time sub-types are interchangeable
    if expected.startswith("datetime.time.") and predicted.startswith("datetime.time."):
        return True
    # Timestamp sub-types are interchangeable
    if expected.startswith("datetime.timestamp.") and predicted.startswith("datetime.timestamp."):
        return True
    # Geographic hierarchy interchangeable
    geo_set = {"geography.location.region", "geography.location.state", "geography.location.continent"}
    if expected in geo_set and predicted in geo_set:
        return True
    # entity_name satisfies full_name GT label
    if expected == "identity.person.full_name" and predicted == "representation.text.entity_name":
        return True
    return False


def is_domain_match(predicted: str, expected_label: str, expected_domain: str) -> bool:
    """Check domain match with interchangeability rules."""
    pred_domain = predicted.split(".")[0] if "." in predicted else ""
    if pred_domain == expected_domain:
        return True
    # entity_name in representation domain satisfies identity-domain "name" GT
    if expected_label == "identity.person.full_name" and predicted == "representation.text.entity_name":
        return True
    return False


def compute_profile_accuracy(
    predictions: list[dict],
    ground_truth: list[dict],
    schema_mapping: dict[str, dict],
) -> dict:
    """Compute profile eval accuracy metrics.

    Matches eval_profile.sql methodology:
    - Headline = format-detectable (direct + close match quality) only
    - Interchangeability rules for boolean, time, timestamp, geography, entity_name
    """
    gt_lookup = {}
    for row in ground_truth:
        gt_lookup[(row["dataset"], row["column_name"])] = row["gt_label"]

    total = 0
    label_correct = 0
    domain_correct = 0
    datasets_seen = set()
    misses = []

    for pred in predictions:
        key = (pred["dataset"], pred["column_name"])
        gt_label = gt_lookup.get(key)
        if gt_label is None:
            continue

        mapping = schema_mapping.get(gt_label)
        if mapping is None:
            continue

        match_quality = mapping.get("match_quality", "")
        # Only count format-detectable types (direct + close) for headline
        if match_quality not in ("direct", "close"):
            continue

        total += 1
        datasets_seen.add(pred["dataset"])
        predicted = pred["predicted_type"]
        expected_label = mapping.get("finetype_label", "")
        expected_domain = mapping.get("finetype_domain", "")

        label_ok = is_label_match(predicted, expected_label)
        if label_ok:
            label_correct += 1

        domain_ok = is_domain_match(predicted, expected_label, expected_domain)
        if domain_ok:
            domain_correct += 1

        if not label_ok:
            misses.append({
                "dataset": pred["dataset"],
                "column": pred["column_name"],
                "predicted": predicted,
                "expected": expected_label,
                "gt_label": gt_label,
                "confidence": float(pred["confidence"]),
            })

    return {
        "total": total,
        "n_datasets": len(datasets_seen),
        "label_correct": label_correct,
        "domain_correct": domain_correct,
        "label_accuracy": round(label_correct / total * 100, 1) if total > 0 else 0,
        "domain_accuracy": round(domain_correct / total * 100, 1) if total > 0 else 0,
        "misses": sorted(misses, key=lambda x: x["confidence"], reverse=True),
    }


def compute_precision_by_type(
    predictions: list[dict],
    ground_truth: list[dict],
    schema_mapping: dict[str, dict],
) -> list[dict]:
    """Compute precision per predicted type on profile eval data.

    Uses format-detectable (direct + close) GT labels with interchangeability rules.
    """
    gt_lookup = {}
    for row in ground_truth:
        gt_lookup[(row["dataset"], row["column_name"])] = row["gt_label"]

    type_counts: dict[str, dict] = defaultdict(lambda: {"predicted": 0, "correct": 0})

    for pred in predictions:
        key = (pred["dataset"], pred["column_name"])
        gt_label = gt_lookup.get(key)
        if gt_label is None:
            continue

        mapping = schema_mapping.get(gt_label)
        if mapping is None or mapping.get("match_quality") not in ("direct", "close"):
            continue

        predicted = pred["predicted_type"]
        type_counts[predicted]["predicted"] += 1

        expected_label = mapping.get("finetype_label", "")
        if is_label_match(predicted, expected_label):
            type_counts[predicted]["correct"] += 1

    results = []
    for ptype, counts in sorted(type_counts.items(), key=lambda x: x[1]["predicted"], reverse=True):
        precision = round(counts["correct"] / counts["predicted"] * 100, 1) if counts["predicted"] > 0 else 0
        if counts["correct"] >= counts["predicted"] * 0.95:
            status = "🟢"
        elif counts["correct"] >= counts["predicted"] * 0.80:
            status = "🟡"
        else:
            status = "🔴"
        results.append({
            "type": ptype,
            "predicted": counts["predicted"],
            "correct": counts["correct"],
            "precision": precision,
            "status": status,
        })
    return results


def generate_report(
    profile_accuracy: dict,
    precision_by_type: list[dict],
    actionability: list[dict],
    taxonomy_stats: dict,
) -> str:
    """Generate markdown report."""
    now = datetime.now().strftime("%Y-%m-%d %H:%M")
    lines = []

    def w(line=""):
        lines.append(line)

    w("# FineType Evaluation Report")
    w()
    w(f"**Generated:** {now}")
    w()

    # ── Headline metrics ──
    w("## Headline Metrics")
    w()

    # Profile accuracy
    pa = profile_accuracy
    label_status = "🟢" if pa["label_accuracy"] >= 90 else ("🟡" if pa["label_accuracy"] >= 80 else "🔴")
    domain_status = "🟢" if pa["domain_accuracy"] >= 95 else ("🟡" if pa["domain_accuracy"] >= 90 else "🔴")

    w("| Metric | Value | Status |")
    w("|---|---|---|")
    w(f"| Profile label accuracy | {pa['label_correct']}/{pa['total']} ({pa['label_accuracy']}%) | {label_status} |")
    w(f"| Profile domain accuracy | {pa['domain_correct']}/{pa['total']} ({pa['domain_accuracy']}%) | {domain_status} |")

    # Actionability
    if actionability:
        total_vals = sum(int(r["total_values"]) for r in actionability)
        success_vals = sum(int(r["parse_success"]) for r in actionability)
        action_rate = round(success_vals / total_vals * 100, 1) if total_vals > 0 else 0
        action_status = "🟢" if action_rate >= 95 else ("🟡" if action_rate >= 80 else "🔴")
        cols_perfect = sum(1 for r in actionability if float(r["success_rate"]) >= 95)
        w(f"| Actionability (datetime) | {success_vals}/{total_vals} ({action_rate}%) | {action_status} |")
        w(f"| Columns with >95% parse rate | {cols_perfect}/{len(actionability)} | |")

    # Taxonomy
    ts = taxonomy_stats
    w(f"| Taxonomy types | {ts['total_types']} | |")
    w(f"| Types with format_string | {ts['with_format_string']} | |")
    w(f"| Types with validation | {ts['with_validation']} | |")
    w(f"| Types with locale validation | {ts['with_locale_validation']} | |")
    w()

    # ── Taxonomy coverage ──
    w("## Taxonomy Coverage")
    w()
    w("| Domain | Types |")
    w("|---|---|")
    for domain in sorted(ts["domains"]):
        w(f"| {domain} | {ts['domains'][domain]} |")
    w()

    # ── Profile eval detail ──
    w("## Profile Evaluation")
    w()
    w(f"**Label accuracy:** {pa['label_correct']}/{pa['total']} ({pa['label_accuracy']}%)")
    w(f"**Domain accuracy:** {pa['domain_correct']}/{pa['total']} ({pa['domain_accuracy']}%)")
    w()

    if pa["misses"]:
        w("### Misclassifications")
        w()
        w("| Dataset | Column | Predicted | Expected | Confidence |")
        w("|---|---|---|---|---|")
        for m in pa["misses"]:
            pred_short = m["predicted"].split(".")[-1]
            exp_short = m["expected"].split(".")[-1] if m["expected"] else m["gt_label"]
            w(f"| {m['dataset']} | {m['column']} | {pred_short} | {exp_short} | {m['confidence']:.2f} |")
        w()

    # ── Precision per type ──
    if precision_by_type:
        w("## Precision Per Type (Profile Eval)")
        w()
        w("| Predicted Type | Predicted | Correct | Precision | Status |")
        w("|---|---|---|---|---|")
        for p in precision_by_type:
            short_type = p["type"].split(".")[-1]
            w(f"| {short_type} | {p['predicted']} | {p['correct']} | {p['precision']}% | {p['status']} |")
        w()

    # ── Actionability detail ──
    if actionability:
        w("## Actionability Evaluation")
        w()
        w("Can analysts safely TRY_CAST using FineType's format_string predictions?")
        w("**Target:** >95% success rate for datetime types")
        w()

        # Summary by type
        type_stats: dict[str, dict] = {}
        for r in actionability:
            t = r["predicted_type"]
            if t not in type_stats:
                type_stats[t] = {"total": 0, "success": 0, "columns": 0}
            type_stats[t]["total"] += int(r["total_values"])
            type_stats[t]["success"] += int(r["parse_success"])
            type_stats[t]["columns"] += 1

        w("### By Type")
        w()
        w("| Type | Columns | Values | Success Rate | Status |")
        w("|---|---|---|---|---|")
        for t in sorted(type_stats, key=lambda x: type_stats[x]["columns"], reverse=True):
            s = type_stats[t]
            rate = round(s["success"] / s["total"] * 100, 1) if s["total"] > 0 else 0
            status = "🟢" if rate >= 95 else ("🟡" if rate >= 80 else "🔴")
            short_type = t.split(".")[-1]
            w(f"| {short_type} | {s['columns']} | {s['total']} | {rate}% | {status} |")
        w()

        # Failures detail
        failures = [r for r in actionability if float(r["success_rate"]) < 95]
        if failures:
            w("### Below Target (<95%)")
            w()
            w("| Dataset | Column | Type | Format | Success Rate |")
            w("|---|---|---|---|---|")
            for r in sorted(failures, key=lambda x: float(x["success_rate"])):
                short_type = r["predicted_type"].split(".")[-1]
                w(f"| {r['dataset']} | {r['column_name']} | {short_type} | `{r['format_string']}` | {r['success_rate']}% 🔴 |")
            w()

    # ── Notes ──
    w("## Evaluation Components")
    w()
    w("| Component | Scope | Target | Status |")
    w("|---|---|---|---|")
    w(f"| Profile regression | {pa['total']} columns, {pa['n_datasets']} datasets | No regressions | {label_status} |")
    w("| Precision per type | SOTAB/GitTables | 🟢≥95% per type | Run `make eval-sotab-cli` |")
    w("| Overcall analysis | SOTAB/GitTables | <5% FP rate | Run `make eval-sotab-cli` |")
    action_line = f"{action_status}" if actionability else "Not run"
    w(f"| Actionability | Profile eval datetime | >95% parse rate | {action_line} |")
    w("| Confidence calibration | SOTAB/GitTables | Gap <10pp | Run `make eval-sotab-cli` |")
    w("| Domain accuracy | SOTAB format-detectable | >80% | Run `make eval-sotab-cli` |")
    w()
    w("---")
    w("*Generated by eval/eval_report.py (NNFT-147)*")
    w()

    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(description="FineType Evaluation Report Generator")
    parser.add_argument(
        "--profile-results",
        default="eval/eval_output/profile_results.csv",
        help="Path to profile results CSV",
    )
    parser.add_argument(
        "--ground-truth",
        default="eval/eval_output/ground_truth.csv",
        help="Path to ground truth CSV",
    )
    parser.add_argument(
        "--schema-mapping",
        default="eval/schema_mapping.csv",
        help="Path to schema mapping CSV",
    )
    parser.add_argument(
        "--actionability-results",
        default="eval/eval_output/actionability_results.csv",
        help="Path to actionability results CSV",
    )
    parser.add_argument(
        "--labels-dir",
        default="labels",
        help="Path to taxonomy YAML directory",
    )
    parser.add_argument(
        "--output",
        default="eval/eval_output/report.md",
        help="Path for output markdown report",
    )
    args = parser.parse_args()

    # Load data
    predictions = load_csv(Path(args.profile_results))
    ground_truth = load_csv(Path(args.ground_truth))
    schema_mapping = load_schema_mapping(Path(args.schema_mapping))
    actionability = load_csv(Path(args.actionability_results))
    taxonomy_stats = load_taxonomy_stats(Path(args.labels_dir))

    print(f"Loaded {len(predictions)} predictions, {len(ground_truth)} ground truth, "
          f"{len(schema_mapping)} mappings, {len(actionability)} actionability results")

    # Compute metrics
    profile_accuracy = compute_profile_accuracy(predictions, ground_truth, schema_mapping)
    precision_by_type = compute_precision_by_type(predictions, ground_truth, schema_mapping)

    print(f"Profile: {profile_accuracy['label_correct']}/{profile_accuracy['total']} "
          f"({profile_accuracy['label_accuracy']}% label, {profile_accuracy['domain_accuracy']}% domain)")

    # Generate report
    report = generate_report(profile_accuracy, precision_by_type, actionability, taxonomy_stats)

    # Write output
    output_path = Path(args.output)
    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(report)
    print(f"Report written to: {output_path}")


if __name__ == "__main__":
    main()
